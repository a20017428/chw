#include <stdio.h>
#include <stdlib.h>

int FindMax(int[]);
int FindMin(int[]);

int fail = 0;

int main(){
	int i;
	double avg = 0.0;
	int score[5];
	
	printf("請輸入5筆成績:\n");
	for(i = 0; i<5; ++i){
		scanf("%d", &score[i]);
		avg+=score[i];
	}
	avg/=5;
	
	printf("成績最高為%d最低為%d\n", FindMax(score), FindMin(score));
	printf("平均成績為%.1lf\n", avg);
	printf("不及格科目為%d科\n", fail);
	fail > (int)(5/2) ? printf("本學期二一*\n") : printf("本學期無二一\n");
	
    system("pause");

    return 0;
}

int FindMax(int score[]){
	int i, max = 0;
	
	for(i = 0; i<5; ++i){
		if(score[i]>max)
			max = score[i];
		if(score[i]<60)
			fail++;
	}
	
	return max;
}

int FindMin(int score[]){
	int i, min = 101;
		
	for(i = 0; i<5; ++i)
		if(score[i]<min)
			min = score[i];
	
	return min;
}


